const generateMockRoom = (
  id: number,
  contactName: string,
  conversationTopic: string,
  brand: string,
  company: string,
  channel: ChannelType,
  status: 'active' | 'waiting' | 'closed',
  contactStatus: ContactStatus,
  additionalAttributes: AdditionalAttribute[],
  mainCategory: 'waiting' | 'responding' | 'closed' | 'alarm' | 'absent',
  additionalCategory: 'request' | 'maintain' | null,
  unreadCount: number | boolean,
  customerStatus: 'online' | 'typing' | 'away',
  isOpen: number | boolean | null = false,
  alarmTimestamp: number | null = null,
  flag: Room['flag'] = null,
  isVIP: boolean = false,
  isFavorite: boolean = false,
  isAIHandled: boolean = false,
  tokenUsage: number | null = null,
  messageCount: number = 5,
  consultantName: string = '김상담',
  duration: string | null = null,
  subject: string = '',
  summary: string = '',
  tags: string[] = [],
  elapsedTimeMs: number | null = null,
  lastActivityMinutesAgo: number = 5
): Room => {
  const messages: Room['messages'] = [];
  const baseTime = Date.now() - (24 * 60 * 60 * 1000);

  const customerMessages = [
    `안녕하세요, ${conversationTopic}에 대해 문의드립니다.`,
    `네, 이해했습니다. 추가로 궁금한 점이 있는데요.`,
    `그 부분이 잘 안되는 것 같아요.`,
    `혹시 다른 방법은 없을까요?`,
    `알겠습니다. 그렇게 해볼게요.`,
    `감사합니다!`,
  ];

  const agentMessages = [
    `안녕하세요 고객님, 무엇을 도와드릴까요?`,
    `Yes, I'll guide you through ${conversationTopic}.`,
    `확인해보겠습니다. 잠시만 기다려주세요.`,
    `다음과 같이 진행하시면 됩니다.`,
    `추가 문의사항이 있으시면 말씀해주세요.`,
    `도움이 되셨다니 다행입니다. 좋은 하루 되세요!`,
  ];

  for (let i = 0; i < messageCount; i++) {
    const timeOffset = i * 10 * 60 * 1000;
    const timestamp = new Date(baseTime + timeOffset);
    const isCustomer = i % 2 === 0;
    const messageIndex = Math.floor(i / 2) % (isCustomer ? customerMessages.length : agentMessages.length);

    messages.push({
      id: id * 100 + i + 1,
      sender: (isCustomer ? 'customer' : 'agent') as 'customer' | 'agent',
      text: i === messageCount - 1 && i % 2 === 0 ? `Final message about ${conversationTopic}` : (isCustomer ? customerMessages[messageIndex] : agentMessages[messageIndex]),
      time: timestamp.toLocaleTimeString('ko-KR', { hour: '2-digit', minute: '2-digit' })
    });
  }

  const startTime = new Date(baseTime).toLocaleString('ko-KR', {
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit'
  });

  const lastActivityTimestamp = Date.now() - (lastActivityMinutesAgo * 60 * 1000);

  return {
    id,
    contactName,
    conversationTopic,
    brand,
    company,
    channel,
    status,
    lastMessage: messages[messages.length - 1]?.text || `Last message about ${conversationTopic}`,
    unreadCount: typeof unreadCount === 'number' ? unreadCount : 0,
    isOpen: !!isOpen,
    currentWorkspaceId: null,
    customerStatus,
    contactStatus,
    additionalAttributes,
    mainCategory,
    additionalCategory,
    isAlarmSet: alarmTimestamp !== null,
    alarmTimestamp,
    flag,
    isVIP: !!isVIP,
    isFavorite: !!isFavorite,
    isAIHandled: !!isAIHandled,
    tokenUsage,
    tempMessage: '',
    consultantName,
    duration,
    subject: subject || conversationTopic,
    startTime,
    summary: summary || `${contactName}님의 ${conversationTopic}에 대한 상담입니다.`,
    tags,
    customerPhone: `010-${String(1000 + (id % 9000)).padStart(4, '0')}-${String(1000 + ((id * 7) % 9000)).padStart(4, '0')}`,
    customerEmail: `${contactName.replace(/\s/g, '').toLowerCase()}@example.com`,
    elapsedTimeMs,
    lastActivityTimestamp,
    lastConversationTimestamp: lastActivityTimestamp,
    messages,
  };
};

const generateHistoricalRoom = (
  id: number,
  contactName: string,
  conversationTopic: string,
  brand: string,
  company: string,
  channel: ChannelType,
  date: string,
  messageCount: number = 12,
  consultantName: string = '김상담',
  duration: string = '25분',
  summary: string = '',
  tags: string[] = [],
  daysAgo: number = 1
): Room => {
  const messages: Room['messages'] = [];

  const customerMessages = [
    `안녕하세요, ${conversationTopic}에 대해 문의드립니다.`,
    `주문번호는 ORD-2024-${id} 입니다.`,
    `네, 알겠습니다.`,
    `혹시 진행상황을 확인할 수 있을까요?`,
    `추가로 문의드릴게요.`,
    `감사합니다. 도움이 많이 되었습니다.`,
  ];

  const agentMessages = [
    `안녕하세요 고객님, ${conversationTopic} 관련 문의시군요.`,
    `확인해보겠습니다. 잠시만 기다려주세요.`,
    `확인 완료되었습니다. 안내드리겠습니다.`,
    `다음과 같이 처리해드렸습니다.`,
    `추가 문의사항이 있으시면 말씀해주세요.`,
    `감사합니다. 좋은 하루 되세요!`,
  ];

  for (let i = 0; i < messageCount; i++) {
    const hour = 10 + Math.floor(i / 4);
    const minute = (i % 4) * 15;
    const isCustomer = i % 2 === 0;
    const messageIndex = Math.floor(i / 2) % (isCustomer ? customerMessages.length : agentMessages.length);

    messages.push({
      id: id * 100 + i + 1,
      sender: (isCustomer ? 'customer' : 'agent') as 'customer' | 'agent',
      text: isCustomer ? customerMessages[messageIndex] : agentMessages[messageIndex],
      time: `${date} ${hour.toString().padStart(2, '0')}:${minute.toString().padStart(2, '0')}`
    });
  }

  const lastActivityTimestamp = Date.now() - (daysAgo * 24 * 60 * 60 * 1000);

  return {
    id,
    contactName,
    conversationTopic,
    brand,
    company,
    channel,
    status: 'closed',
    lastMessage: messages[messages.length - 1]?.text || `Completed consultation about ${conversationTopic}`,
    unreadCount: 0,
    isOpen: false,
    currentWorkspaceId: null,
    customerStatus: 'away',
    contactStatus: 'closed',
    additionalAttributes: [],
    mainCategory: 'closed',
    additionalCategory: null,
    isAlarmSet: false,
    alarmTimestamp: null,
    flag: null,
    isVIP: false,
    isFavorite: false,
    isAIHandled: false,
    tokenUsage: null,
    tempMessage: '',
    consultantName,
    duration,
    subject: conversationTopic,
    startTime: `${date} 10:00`,
    summary: summary || `${contactName}님의 ${conversationTopic}에 대한 상담이 완료되었습니다.`,
    tags,
    elapsedTimeMs: null,
    lastActivityTimestamp,
    lastConversationTimestamp: lastActivityTimestamp,
    messages,
  };
};

