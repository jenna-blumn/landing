type ContactStatus = 'ai-response' | 'assignment-waiting' | 'responding' | 'closed';
type AdditionalAttribute = 'alarm' | 'maintain' | 'request';

type ChannelType = 'chat' | 'phone' | 'kakao' | 'naver' | 'instagram' | 'board' | 'email';

interface Room {
  id: number;
  contactName: string;
  conversationTopic: string;
  brand: string;
  company: string;
  channel: ChannelType;
  status: 'active' | 'waiting' | 'closed';
  lastMessage: string;
  unreadCount: number;
  isOpen: boolean;
  currentWorkspaceId: number | null;
  customerStatus: 'online' | 'typing' | 'away';
  contactStatus: ContactStatus;
  additionalAttributes: AdditionalAttribute[];
  mainCategory: 'waiting' | 'received' | 'responding' | 'closed' | 'alarm' | 'absent';
  additionalCategory: 'request' | 'maintain' | null;
  isAlarmSet: boolean;
  alarmTimestamp: number | null;
  flag: {
    type: 'urgent' | 'important' | 'normal' | 'info' | 'completed' | null;
    color: string;
    label: string;
  } | null;
  isVIP: boolean;
  isFavorite: boolean;
  isAIHandled: boolean;
  tokenUsage: number | null;
  tempMessage: string;
  consultantName: string;
  duration: string | null;
  subject: string;
  startTime: string;
  summary: string;
  tags: string[];
  elapsedTimeMs: number | null;
  lastActivityTimestamp: number;
  lastConversationTimestamp: number;
  customerPhone?: string;
  customerEmail?: string;
  messages: Array<{
    id: number;
    sender: 'customer' | 'agent';
    text: string;
    time: string;
    channel?: 'chat' | 'sms' | 'alimtalk' | 'email';
    subject?: string;
    templateId?: string;
    templateVariables?: Record<string, string>;
    byteLength?: number;
    status?: 'sending' | 'sent' | 'delivered' | 'failed';
    deliveryMode?: 'immediate' | 'scheduled';
    schedule?: {
      scheduledAt: string;
      queuedAt: string;
      status: 'scheduled' | 'sending' | 'sent' | 'failed' | 'canceled';
      sentAt?: string;
      canceledAt?: string;
      errorMessage?: string;
    };
  }>;
}

